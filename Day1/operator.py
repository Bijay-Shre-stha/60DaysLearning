#Arithmetic Operators
number = 1 + 2 * 3 / 4
print(number)

#Modulus
remainder = 8 % 3
print(remainder)

#String Operators
hello = "Hello"
world = "World"
helloWorld = hello + " " + world
print(helloWorld)

#String Repetition
helloWorld = helloWorld * 2
print(helloWorld)

#List Operators
list1 = [1,2,3]
list2 = [4,5,6]
list3 = list1 + list2
print(list3)

